export type DocumentType = 'phytosanitary' | 'certificate-of-origin' | 'bill-of-lading' | 'packing-list' | 'commercial-invoice' | 'fumigation-cert' | 'quality-cert' | 'customs-declaration';
export type DocumentStatus = 'not-started' | 'in-progress' | 'submitted' | 'approved' | 'rejected';
export type ShipmentStatus = 'preparing' | 'customs-clearance' | 'in-transit' | 'at-port' | 'delivered';

export interface ExportDocument {
  id: string;
  shipmentId: string;
  type: DocumentType;
  title: string;
  status: DocumentStatus;
  issuedBy: string;
  issuedDate: string | null;
  expiryDate: string | null;
  reference: string;
  notes: string;
}

export interface ExportShipment {
  id: string;
  exporterId: string;
  exporterName: string;
  product: string;
  quantity: string;
  destination: string;
  destinationPort: string;
  originPort: string;
  vessel: string | null;
  status: ShipmentStatus;
  estimatedDeparture: string;
  estimatedArrival: string;
  actualDeparture: string | null;
  value: number;
  currency: string;
  documents: string[];
  timeline: { date: string; event: string; location: string }[];
}

