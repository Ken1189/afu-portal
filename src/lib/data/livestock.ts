// ===== SUMMARY FUNCTION =====
export function getLivestockSummary(): LivestockSummary {
  const activeAnimals = animals.filter(
    (a) => a.status !== 'sold' && a.status !== 'deceased'
  );

  const totalAnimals = activeAnimals.length;
  const totalValue = activeAnimals.reduce((sum, a) => sum + a.currentValue, 0);

  const byType: Record<AnimalType, number> = {
    cattle: 0,
    goat: 0,
    sheep: 0,
    poultry: 0,
    pig: 0,
  };
  for (const animal of activeAnimals) {
    byType[animal.type]++;
  }

  const healthyCount = activeAnimals.filter((a) => a.status === 'healthy').length;
  const healthyPercentage = totalAnimals > 0
    ? Math.round((healthyCount / totalAnimals) * 100)
    : 0;

  const pregnantCount = activeAnimals.filter((a) => a.status === 'pregnant').length;

  const today = new Date().toISOString().split('T')[0];
  const thirtyDaysFromNow = new Date(
    Date.now() + 30 * 24 * 60 * 60 * 1000
  ).toISOString().split('T')[0];

  const upcomingVaccinations = vetRecords.filter(
    (v) =>
      v.type === 'vaccination' &&
      v.nextDueDate !== null &&
      v.nextDueDate >= today &&
      v.nextDueDate <= thirtyDaysFromNow
  ).length;

  return {
    totalAnimals,
    totalValue,
    byType,
    healthyPercentage,
    pregnantCount,
    upcomingVaccinations,
  };
}
