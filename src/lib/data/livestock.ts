export type AnimalType = 'cattle' | 'goat' | 'sheep' | 'poultry' | 'pig';
export type AnimalStatus = 'healthy' | 'sick' | 'pregnant' | 'lactating' | 'quarantine' | 'sold' | 'deceased';

export interface Animal {
  id: string;
  name: string;
  type: AnimalType;
  breed: string;
  tag: string; // ear tag number
  dateOfBirth: string;
  gender: 'male' | 'female';
  weight: number; // kg
  status: AnimalStatus;
  parentSire: string | null;
  parentDam: string | null;
  acquisitionDate: string;
  acquisitionMethod: 'born' | 'purchased' | 'donated';
  purchasePrice: number | null;
  currentValue: number;
  image: string; // unsplash
  notes: string;
}

export interface VetRecord {
  id: string;
  animalId: string;
  animalName: string;
  type: 'vaccination' | 'treatment' | 'checkup' | 'surgery' | 'deworming' | 'dipping';
  description: string;
  date: string;
  veterinarian: string;
  clinic: string;
  cost: number;
  nextDueDate: string | null;
  medications: string[];
  notes: string;
}

export interface BreedingRecord {
  id: string;
  sireId: string;
  sireName: string;
  damId: string;
  damName: string;
  matingDate: string;
  expectedDueDate: string;
  actualBirthDate: string | null;
  method: 'natural' | 'artificial-insemination';
  status: 'mated' | 'confirmed-pregnant' | 'delivered' | 'failed';
  offspring: { id: string; name: string; gender: 'male' | 'female' }[];
  notes: string;
}

export interface LivestockSummary {
  totalAnimals: number;
  totalValue: number;
  byType: Record<AnimalType, number>;
  healthyPercentage: number;
  pregnantCount: number;
  upcomingVaccinations: number;
}

