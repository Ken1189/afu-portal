export type TransportType = 'refrigerated' | 'dry-bulk' | 'flatbed' | 'container' | 'pickup';
export type BookingStatus = 'requested' | 'confirmed' | 'in-transit' | 'delivered' | 'cancelled';

export interface TransportBooking {
  id: string;
  farmerId: string;
  farmerName: string;
  type: TransportType;
  carrierId: string;
  carrierName: string;
  origin: string;
  destination: string;
  cargo: string;
  weight: number; // kg
  status: BookingStatus;
  requestDate: string;
  pickupDate: string;
  deliveryDate: string | null;
  estimatedCost: number;
  actualCost: number | null;
  currency: string;
  temperature?: { min: number; max: number };
  notes: string;
  timeline: { date: string; event: string; location: string }[];
}

export interface Carrier {
  id: string;
  name: string;
  type: TransportType[];
  country: string;
  rating: number;
  pricePerKm: number; // USD per km per tonne
  coldChain: boolean;
  insurance: boolean;
  vehicleCount: number;
}

